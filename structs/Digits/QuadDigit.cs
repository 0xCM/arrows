namespace Z0
{
    using System;
    using System.Collections.Generic;

    /// <summary>
    /// Defines identifies for digits in base-4
    /// </summary>
    public enum QuadDigit : byte
    {
        /// <summary>
        /// Identifies 0b00
        /// </summary>
        A = 0b00,
        
        /// <summary>
        /// Identifies 0b01
        /// </summary>
        B = 0b01,

        /// <summary>
        /// Identifies 0b10
        /// </summary>
        C = 0b10,

        /// <summary>
        /// Identifies 0b11
        /// </summary>
        D = 0b11

    }

}